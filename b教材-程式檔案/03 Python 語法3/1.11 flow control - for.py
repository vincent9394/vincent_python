# list of tuples - getting elements inside tuple
a = [ (0, "a"), (1, "b"), (2, "c") ]
for i in a:    
    print( i[0] )      
    print( i[1] )     
	# i[0] 會等於 0, 1, 2，型別是整數，i[1] 會等於 "a", "b", "c"，型別是字串。
	# 但由於整數無法和字串直接相加，因此我們要用 str() 函數把 i[0] 轉換為字串，
	# 才能夠把 i[0] 、 ", " 、 i[1] 三個資料相加打印出屏幕。


	
	
# list of tuples - 2 variables
a = [ (0, "a"), (1, "b"), (2, "c") ]
for i, j in a:        # 原來我們可以用兩個變數儲存 Tuple 內每個元素！
    print( i )
    print( j )   
    #for 用於 list of tuples 時， i, j 會順序等於 Tuple 內每個元素

	
	
# list of tuples - 3 variables
a = [ (0, "a", "a1"), (1, "b", "b1"), (2, "c", "c1") ]
for i, j, k in a:        # 用三個變數儲存 Tuple 內每個元素
    print( i )
    print( j )   
    print( k )    
    # for 用於 list of tuples 時， i, j, k 會順序等於 Tuple 內每個元素



    

