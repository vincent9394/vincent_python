# list
a = [0.1, 0.5, 0.25, 0.8, 0.9]
for i in a:        # i 為每個元素
    print( i*100 )    
    
	
# tuple
a = (0.1, 0.5, 0.25, 0.8, 0.9)
for i in a:        # i 為每個元素
    print( i*100 )
    

# string
a = "ABCDEFG"
for i in a:        # i 為每個字母
    print( i )
    

# dictionary
a = { "id": "1", "name": "benson" }
for i in a:
    print( i )      # i 為鍵(key)，a[i] 為值
    print( a[i] )

	

# Common use of for loop in data processing
a = [0.1, 0.5, 0.25, 0.8, 0.9]            # 待轉換成百分比的數值
percent = []                        # 定義空 list
for i in a:   # 把 a 內的數值逐一乘上100，然後儲存到 percent
    percent.append( i * 100 )
	
	

# for loop example
for i in range(5):
    print( i )
    
	
	
